exemplo = 'Conceitos avançados em Python' # conta os caracteres + espaços
print(exemplo) # mostra o conteúdo completo da variável

print(exemplo[0]) # exibe o primeiro caractere da variável
print(exemplo[0:15]) # exibe do primeiro ao 14 caractere da variável

print(exemplo[0:15:3]) # exibe do primeiro ao 14 caractere, pulando de 3 em 3
print(exemplo[0::3]) # exibe do primeiro ao último caractere, pulando de 3 em 3

print(exemplo[:15]) # exibe do primeiro ao 14 caractere da variável
print(exemplo[15:]) # exibe do 14 caractere até o último da variável