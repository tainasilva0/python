exemplo = 'Conceitos avançados em Python'
print(exemplo)

print(exemplo.replace('Conceitos', 'Manipulações')) # substituição

print(exemplo.upper()) # converter caracteres em maiúsculo
print(exemplo.lower()) # converter caracteres em minúsculo
print(exemplo.capitalize()) # primeiro caractere em maiúsculo, restante minúsculo
print(exemplo.title()) # primeira letra de cada palavra, maiúscula